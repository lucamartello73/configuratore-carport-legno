export function AdminSidebar({ configuratorType }: AdminSidebarProps) {
  const pathname = usePathname()
  
  return (
    <aside
      className="fixed left-0 top-0 h-screen overflow-y-auto"
      style={{
        width: '260px',
        backgroundColor: adminColors.sidebarBg,
        borderRight: `1px solid ${adminColors.border}`,
      }}
    >
      {/* Logo */}
      <div
        className="px-6 py-6 border-b"
        style={{ borderColor: adminColors.border }}
      >
        <h1
          className="text-xl font-bold"
          style={{ color: adminColors.primary }}
        >
          {configuratorType === 'ferro' ? 'Carport Ferro' : 'Pergola Legno'}
        </h1>
        <p
          className="text-xs mt-1"
          style={{ color: adminColors.textSecondary }}
        >
          Pannello Amministrazione
        </p>
      </div>
      
      {/* Navigation */}
      <nav className="px-3 py-4">
        {navItems.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
          
          return (
            <Link
              key={item.id}
              href={item.href}
              className="flex items-center gap-3 px-3 py-2.5 mb-1 rounded-lg transition-all duration-200"
              style={{
                backgroundColor: isActive ? adminColors.sidebarActive : 'transparent',
                color: isActive ? adminColors.sidebarActiveText : adminColors.textPrimary,
              }}
              onMouseEnter={(e) => {
                if (!isActive) {
                  e.currentTarget.style.backgroundColor = adminColors.sidebarHover
                }
              }}
              onMouseLeave={(e) => {
                if (!isActive) {
                  e.currentTarget.style.backgroundColor = 'transparent'
                }
              }}
            >
              {item.icon}
              <span className="text-sm font-medium">{item.label}</span>
            </Link>
          )
        })}
      </nav>
    </aside>
  )
}
